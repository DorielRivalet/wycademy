// TODO mocha pots svg
export function getItemIcon(iconName: string) {
	const icon = itemInfo[5].icon;

	const found = itemInfo.find((w) => w.name === iconName);
	if (!found) {
		return icon;
	}

	return found.icon;
}

export function getItemColor(color: FrontierItemColor) {
	const fallback = ItemColors[0].value;

	const found = ItemColors.find((w) => w.name === color);
	if (!found) {
		return fallback;
	}

	return found.value;
}
