export const achievementRankColors = [
	'var(--ctp-base)',
	'var(--ctp-maroon)',
	'var(--ctp-lavender)',
	'var(--ctp-yellow)',
	'var(--ctp-teal)',
];

export function getAchievementRankColor(rank: number) {
	if (rank >= achievementRankColors.length || rank < 0) {
		return achievementRankColors[0];
	} else {
		return achievementRankColors[rank];
	}
}

export function getAchievementImage(rank: number) {
	switch (rank) {
		default:
			return TrophySecret;
		case 1:
			return TrophyBronze;
		case 2:
			return TrophySilver;
		case 3:
			return TrophyGold;
		case 4:
			return TrophyPlatinum;
	}
}

